package io.github.ymatojava.crawler.core.search;

import java.util.List;

/**
 * Интерфейс поискового движка.
 * <p>
 * Определяет контракт для поиска проиндексированных веб-страниц по текстовому запросу.
 * Абстракция через интерфейс позволяет подменять реализацию: например, заменить
 * in-memory индекс на Elasticsearch или Apache Lucene без изменения клиентского кода.
 */
public interface SearchEngine {

    /**
     * Выполняет поиск по текстовому запросу и возвращает список результатов,
     * отсортированных по убыванию релевантности.
     *
     * @param query Поисковый запрос (одно или несколько слов).
     *              Все термины объединяются логической операцией AND:
     *              результат должен содержать ВСЕ слова из запроса.
     * @param limit Максимальное количество результатов для возврата.
     *              Используется для пагинации и ограничения нагрузки на сеть.
     * @return Список результатов поиска, отсортированных по убыванию {@code relevanceScore}.
     *         Пустой список, если совпадений не найдено.
     */
    List<SearchResult> search(String query, int limit);
}
