package io.github.ymatojava.crawler.core.engine;

import java.util.Collection;
import java.util.Optional;

/**
 * Интерфейс фронтира URL — центральной структуры данных BFS-краулера.
 *
 * <p>Фронтир совмещает в себе две функции:
 * <ul>
 *     <li><b>Очередь</b> (FIFO) — определяет порядок обхода URL (поуровневый BFS)</li>
 *     <li><b>Множество посещённых</b> — предотвращает повторную загрузку уже обработанных страниц
 *         и попадание в бесконечные циклы (spider traps)</li>
 * </ul>
 *
 * <p>Текущая версия не требует потокобезопасности — обход выполняется в одном потоке.
 * Для распределённой версии будет создана реализация на базе Redis или Kafka.</p>
 */
public interface UrlFrontier {

    /**
     * Добавляет URL в очередь, если он ещё не был обнаружен ранее.
     * URL, которые уже проходили через фронтир, тихо игнорируются.
     *
     * @param url URL для добавления
     */
    void add(String url);

    /**
     * Добавляет коллекцию URL в очередь, пропуская уже обнаруженные.
     * Удобен для пакетного добавления ссылок, извлечённых со страницы.
     *
     * @param urls коллекция URL для добавления
     */
    void addAll(Collection<String> urls);

    /**
     * Извлекает следующий URL для обработки из головы очереди.
     *
     * @return Optional с URL, либо Optional.empty(), если очередь пуста
     */
    Optional<String> poll();

    /**
     * Проверяет, пуста ли очередь необработанных URL.
     *
     * @return true, если очередь пуста
     */
    boolean isEmpty();

    /**
     * Возвращает текущий размер очереди (количество URL, ожидающих обработки).
     *
     * @return количество URL в очереди
     */
    int size();

    /**
     * Возвращает общее количество обнаруженных URL за сессию обхода,
     * включая уже обработанные и находящиеся в очереди.
     *
     * @return общее число обнаруженных URL
     */
    int totalDiscovered();

    /**
     * Проверяет, проходил ли данный URL через фронтир ранее.
     *
     * @param url URL для проверки
     * @return true, если URL уже был добавлен в фронтир
     */
    boolean alreadySeen(String url);
}
