package io.github.ymatojava.crawler.core.keyword;

import java.util.Collections;
import java.util.List;
import java.util.regex.Pattern;

/**
 * Утилитный класс для токенизации текста.
 * Разбивает входной текст на отдельные слова (токены), применяя следующие правила:
 * <ul>
 *     <li>Разделение происходит по любым символам, не являющимся буквами (латинскими или кириллическими).</li>
 *     <li>Все токены приводятся к нижнему регистру для обеспечения регистронезависимого сравнения.</li>
 *     <li>Пустые строки, которые могут возникнуть при наличии нескольких разделителей подряд, отфильтровываются.</li>
 * </ul>
 * Результат возвращается в виде неизменяемого списка, что гарантирует потокобезопасность
 * при использовании в многопоточной среде распределённого краулера.
 */
public class TextTokenizer {

    /**
     * Регулярное выражение для разбиения текста.
     * Паттерн {@code [^a-zA-Zа-яА-ЯёЁ]+} означает: один или более символов, НЕ являющихся буквами.
     * Поддерживаются как латинские (a-zA-Z), так и кириллические (а-яА-ЯёЁ) символы,
     * что необходимо для корректной обработки русскоязычных страниц.
     * Паттерн предварительно компилируется для повышения производительности при многократном вызове.
     */
    private static final Pattern SPLIT_PATTERN = Pattern.compile("[^a-zA-Zа-яА-ЯёЁ]+");

    /**
     * Разбивает входной текст на список токенов (слов).
     * <p>
     * Алгоритм:
     * <ol>
     *     <li>Если текст пустой или null — возвращается пустой неизменяемый список.</li>
     *     <li>Текст разбивается по предварительно скомпилированному регулярному выражению.</li>
     *     <li>Каждый фрагмент приводится к нижнему регистру.</li>
     *     <li>Пустые строки отфильтровываются (они возникают, если текст начинается с разделителя).</li>
     * </ol>
     *
     * @param text Исходный текст для токенизации. Может быть null.
     * @return Неизменяемый список токенов в нижнем регистре
     */
    public List<String> tokenize(String text) {
        // Быстрый возврат для пустого ввода без аллокаций
        if (text == null || text.isBlank()) {
            return Collections.emptyList();
        }

        // Разбиение → приведение к нижнему регистру → фильтрация пустых → сбор в неизменяемый список
        return SPLIT_PATTERN.splitAsStream(text)
                .map(String::toLowerCase)
                .filter(token -> !token.isEmpty())
                .toList(); // toList() в Java 16+ возвращает неизменяемый список
    }
}
