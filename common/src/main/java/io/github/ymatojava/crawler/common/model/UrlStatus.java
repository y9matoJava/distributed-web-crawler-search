package io.github.ymatojava.crawler.common.model;

/**
 * Перечисление возможных состояний URL в жизненном цикле краулера.
 *
 * Жизненный цикл URL:
 * <pre>
 *   DISCOVERED → CRAWLED   (если страница успешно скачана и обработана)
 *   DISCOVERED → FAILED    (если произошла ошибка при скачивании)
 *   DISCOVERED → SKIPPED   (если URL заблокирован robots.txt или rate-limiter)
 * </pre>
 *
 * Использование enum вместо строковых констант обеспечивает:
 * - типобезопасность на этапе компиляции;
 * - автодополнение в IDE;
 * - защиту от опечаток в коде.
 */
public enum UrlStatus {

    /**
     * URL обнаружен, но ещё не обработан.
     * Находится в очереди на скачивание.
     */
    DISCOVERED,

    /**
     * URL успешно обработан: страница скачана, распарсена, проиндексирована.
     */
    CRAWLED,

    /**
     * Обработка URL завершилась ошибкой (таймаут, 4xx/5xx, невалидный HTML).
     */
    FAILED,

    /**
     * URL пропущен по правилам ответственного краулинга
     * (запрещён в robots.txt, превышена глубина, чужой домен).
     */
    SKIPPED
}
